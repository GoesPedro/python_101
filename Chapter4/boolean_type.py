# Boolean is binary
# In Python, starts with capital letter
active = False
print(f"active = {active}")

# not:
print(f"not active = {not active}")

# or:
print(f"active or not active = {active or not active}")
print(f"active or active = {active or active}")

# and:
print(f"active and not active = {active and not active}")
print(f"not active and not active = {not active and not active}")

# Comparison:
print(f"5 > 6 -> {5 > 6}")
print(f"5 > 4 and 5 < 6 -> {5 > 4 and 5 < 6}")
