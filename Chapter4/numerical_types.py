num = 2
print(f"num = {num}")
print(f"27/num = {27/num}")
print(f"int(27/num) = {int(27/num)}")
# // returns the integer result of the division
print(f"27//num = {27//num}")
# potentiation
print(f"num**10 = {num**10}")
print(f"1_000_000 = {1_000_000}")
num += 1
print(f"num += 1 -> num = {num}")
num *= 2
print(f"num *= 2 -> num = {num}")
print(f"type(num) = {type(num)}")
print(f"type(num/3) = {type(num/3)}")
